let age = "pramod";// This is a string literal, not a number literal
let isStudent = true;// This is a boolean literal
let price = 100.50;// This is a number literal
let name = 'Alice'// This is a string literal using single quotes
let nullValue = null;// This is a null literal assigned to the variable nullValue
let undefinedValue;// This variable is declared but not initialized, so it is an undefined literal

console.log(typeof age);
console.log(typeof isStudent); // Output: "boolean"
console.log(typeof price); // Output: "number"
console.log(typeof name); // Output: "string"
console.log(typeof nullValue); // Output: "object"
console.log(typeof undefinedValue); // Output: "undefined"