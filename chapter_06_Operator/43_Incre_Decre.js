// Pre increment 
let a = 10;
let b = ++a; // first we will increment the value of a and then we will assign it to b
console.log(a); // 11
console.log(b); // 11

// Decrement Operator 
let a = 10;
let b = --a; // first we will decrement the value of a and then we will assign it to b
console.log(a); // 9
console.log(b); // 9

let a = 10;
let b = a--; // first we will decrement the value of a and then we will assign it to b
console.log(a); // 9
console.log(b); // 9